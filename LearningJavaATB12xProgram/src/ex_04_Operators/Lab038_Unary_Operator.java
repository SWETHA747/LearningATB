package ex_04_Operators;

public class Lab038_Unary_Operator {

	public static void main(String[] args) {
		
		//Unary Operator
		int s = 10;
		int t = -110;
		int res = s+t;
		
		System.out.println(s);
		System.out.println(t);
		System.out.println(res);
		
		int b = -1;
		b= b+1;
		System.out.println(b);

	}

}
