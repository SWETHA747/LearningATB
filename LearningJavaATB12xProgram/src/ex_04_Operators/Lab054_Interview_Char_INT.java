package ex_04_Operators;

public class Lab054_Interview_Char_INT {

	public static void main(String[] args) {

		char a = 'A'; // ASCII -> 65
        char b = 'B'; // 66
        char c = 'C'; //67
        System.out.println(a + b);
        // a+b - 65+66 -> 131
        
        System.out.println(a + c);
        //a+c - 65+67  -> 132
	}

}
